const categoryBySlug = (slug: string): CategorySummary => {
  const category = categories.find((entry) => entry.slug === slug);
  if (!category) {
    throw new Error('Catalogue category is missing');
  }
  return category;
};

const sellerBySlug = (slug: string): SellerSummary => {
  const seller = sellers.find((entry) => entry.slug === slug);
  if (!seller) {
    throw new Error('Catalogue seller is missing');
  }
  return seller;
};

interface ProductSeed {
  readonly id: string;
  readonly name: string;
  readonly slug: string;
  readonly shortDescription: string;
  readonly description: string;
  readonly categorySlug: string;
  readonly sellerSlug: string;
  readonly priceInPaise: number;
  readonly compareAtPriceInPaise?: number;
  readonly rating: number;
  readonly reviewCount: number;
  readonly imageUrl: string;
  readonly material: string;
  readonly availableMaterials?: readonly string[];
  readonly colours: readonly string[];
  readonly finish?: string;
  readonly dimensions: string;
  readonly weightGrams: number;
  readonly processingDays: number;
  readonly estimatedDeliveryDays?: number;
  readonly customisable?: boolean;
  readonly stock: number;
  readonly highlights: readonly string[];
  readonly tags?: readonly string[];
  readonly searchKeywords?: readonly string[];
  readonly createdAt: string;
  readonly popularity: number;
}

const createVariants = (seed: ProductSeed): readonly ProductVariantSummary[] =>
  seed.colours.slice(0, 3).map((colour, index) => ({
    id: `${seed.id}-variant-${index + 1}`,
    name: `${colour} ${seed.finish ?? 'Matte'}`,
    material: seed.availableMaterials?.[0] ?? seed.material,
    colour,
    finish: seed.finish ?? 'Matte',
    priceDeltaInPaise: index === 2 ? 5000 : 0,
  }));

const createProduct = (seed: ProductSeed): CatalogueProduct => ({
  id: seed.id,
  name: seed.name,
  slug: seed.slug,
  shortDescription: seed.shortDescription,
  description: seed.description,
  category: categoryBySlug(seed.categorySlug),
  seller: sellerBySlug(seed.sellerSlug),
  priceInPaise: seed.priceInPaise,
  ...(seed.compareAtPriceInPaise === undefined
    ? {}
    : { compareAtPriceInPaise: seed.compareAtPriceInPaise }),
  currency: 'INR',
  rating: seed.rating,
  reviewCount: seed.reviewCount,
  imageUrl: seed.imageUrl,
  gallery: [seed.imageUrl, seed.imageUrl, seed.imageUrl],
  material: seed.material,
  availableMaterials: seed.availableMaterials ?? [seed.material],
  colours: seed.colours,
  finish: seed.finish ?? 'Matte',
  dimensions: seed.dimensions,
  weightGrams: seed.weightGrams,
  processingDays: seed.processingDays,
  estimatedDeliveryDays: seed.estimatedDeliveryDays ?? seed.processingDays + 4,
  customisable: seed.customisable ?? false,
  stock: seed.stock,
  highlights: seed.highlights,
  tags: seed.tags ?? [],
  searchKeywords: seed.searchKeywords ?? [],
  createdAt: seed.createdAt,
  popularity: seed.popularity,
  variants: createVariants(seed),
});

